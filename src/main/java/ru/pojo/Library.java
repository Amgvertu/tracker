package ru.pojo;

public class Library {
    public static void main(String[] args) {
        Book kolobok = new Book("Колобок", (short) 40);
        Book humorBook = new Book("Анекдоты", (short) 156);
        Book goldFish = new Book("Золотая рыбка", (short) 52);
        Book cleanCode = new Book("Clean Code", (short) 0);
        Book[] books = new Book[4];
        books[0] = kolobok;
        books[1] = humorBook;
        books[2] = goldFish;
        books[3] = cleanCode;
        System.out.println("Первоначальный массив");
        for (int i = 0; i < books.length; i++) {
            System.out.println(books[i].getTitle() + ", " + books[i].getPage());
        }
        System.out.println("Поменяли 1 и 3 элементы массива");
        Book exchangBook = books[0];
        books[0] = books[2];
        books[2] = exchangBook;
        for (int i = 0; i < books.length; i++) {
            System.out.println(books[i].getTitle() + ", " + books[i].getPage());
        }
        System.out.println("Вывод только книг CleanCode");
        for (int i = 0; i < books.length; i++) {
            if (books[i].getTitle().equals("Clean Code")) {
                System.out.println(books[i].getTitle() + ", " + books[i].getPage());
            }
        }

    }
}




































































































































































































































































































































































































































































































































































































































































































































