package ru.pojo;

public class Library {
    public static void main(String[] args) {
        Book[] books = {
                new Book("Колобок", (short) 40),
                new Book("Анекдоты", (short) 156),
                new Book("Золотая рыбка", (short) 52),
                new Book("Clean Code", (short) 0)
        };
        for (int i = 0; i < books.length; i++) {
            System.out.println(books[i].getTitle() + ", " + books[i].getPage());
        }
        System.out.println("Поменяли 1 и 3 элементы массива");
        Book exchangBook = books[0];
        books[0] = books[2];
        books[2] = exchangBook;
        for (int i = 0; i < books.length; i++) {
            System.out.println(books[i].getTitle() + ", " + books[i].getPage());
        }
        System.out.println("Вывод только книг Clean  Code");
        for (int i = 0; i < books.length; i++) {
            if ("Clean Code".equals(books[i].getTitle())) {
                System.out.println(books[i].getTitle() + ", " + books[i].getPage());
            }
        }
    }
}




































































































































































































































































































































































































































































































































































































































































































































